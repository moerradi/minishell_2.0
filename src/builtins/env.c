#include "../../headers/minishell.h"

int	env(int fd)
{
	int	i;

	i = 0;
	while (g_env[i])
	{
		if (g_env[i][0] != '?' && g_env[i][0] != '-')
		{
			ft_putstr_fd(g_env[i], fd);
			ft_putstr_fd("\n", fd);
		}
		i++;
	}
	return (0);
}
